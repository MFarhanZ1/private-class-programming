class Soal05IFElsePacaran{
    public static void main(String[] args) {
        String jawaban = "mau";

        if (jawaban == "mau") {
            System.out.println("Yeay, kita jadian!");
        } else if (jawaban == "enggak") {
            System.out.println("Yaudah, jangan asing ya!");
        } else {
            System.out.println("Apasih jawabanmu itu?");
        }
    }
}